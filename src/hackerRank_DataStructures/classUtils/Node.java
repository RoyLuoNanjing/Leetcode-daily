package hackerRank_DataStructures.classUtils;

public class Node {
    public int data;
    public Node left;
    public Node right;
    Node() {}
    public Node(int val) { this.data = data; }
    Node(int val, Node left, Node right) {
        this.data = data;
        this.left = left;
        this.right = right;     }
}